#include "libft.h"

int	ft_strlen(char *s)
{
	int	len;

	len = 0;
	while (s[len] != '\0')
		len++;
	return (len);
}

void	ft_free2(void *s)
{
	if (s)
	{
		free(s);
		s = NULL;
	}
}
